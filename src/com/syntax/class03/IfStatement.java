package com.syntax.class03;

public class IfStatement {
    public static void main(String[] args) {
        int num1 = 100;
        int num2 = 50;

        // if else conditions:
        if(num1>num2){
            System.out.println(num1+" is larger than "+num2);
        } else {
            System.out.println(num2+" is larger than "+num1);
        }
    }
}
