package com.syntax.class03;

public class EvenOddNumHW {
    public static void main(String[] args) {
        int num =1000;
        if(num % 2 == 0){
            System.out.println("The number "+num+" is an EVEN number!");
        } else {
            System.out.println("The number "+num+" is an ODD number!");
        }
    }
}
