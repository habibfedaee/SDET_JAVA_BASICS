package com.syntax.class01;
public class PracticeVariables {
    public static void main(String[] args){
        // Primitive Data Type Variables > Numeric:
        byte byteVarName = 29;
        short shortVarName = 193;
        int intVarName = 238842;
        long longVarName = 23234321L;
        float floatVarName = 232.33f;
        double doubleVarName = 2323232.99;
        //Non-numeric:
        char charVarName = 'X';
        boolean booleanVarName = true;
        //Printing variables to the console:
        System.out.println(byteVarName);
        System.out.println(shortVarName);
        System.out.println(intVarName);
        System.out.println(longVarName);
        System.out.println(floatVarName);
        System.out.println(doubleVarName);
        System.out.println(charVarName);
        System.out.println(booleanVarName);
    }
}
