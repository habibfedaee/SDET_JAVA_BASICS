package com.syntax.class02;

public class CreatingVariables {
    public static void main(String[] args){
        // Declare a var:
        int age;

        // Assign a value to it:
        age =25;

        // Multiple Variabl Declaration:
        int n1, n2, n3;

        // initializing the vars:
        n1 = 10;
        n2 = 20;
        n3 = 30;

        /*
        * variable and methods names should start with lowercase
        * give variable a meaningful name
        *
        * class name should start with upper case
        *
        * if name has 2 or more words --> follow camel case
        * */

        int bigNumber = 123456;

        // Use the var
        System.out.println(age);
        System.out.println();
    }
}
